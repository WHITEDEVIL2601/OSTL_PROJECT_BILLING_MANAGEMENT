def Ref():
    x=random.randint(12980, 50876)
    randomRef = str(x)
    rand.set(randomRef)
    cof =float(Fries.get())
    colfries= float(Largefries.get())
    cob= float(Burger.get())
    cofi= float(Filet.get())
    cochee= float(Cheese_burger.get())
    costoffries = cof*25
    costoflargefries = colfries*40
    costofburger = cob*35
    costoffilet = cofi*50
    costofcheeseburger = cochee*50
   
    subtotal=(costoffries +  costoflargefries + costofburger + costoffilet  + costofcheeseburger)
    PayTax=((costoffries +  costoflargefries + costofburger + costoffilet +  costofcheeseburger )*0.05)
    Totalcost=(costoffries +  costoflargefries + costofburger + costoffilet  + costofcheeseburger )

    OverAllCost="Rs.",str( PayTax + Totalcost )
    PaidTax="Rs.",str('%.2f'% PayTax)
    Tax.set(PaidTax)
    Subtotal.set(subtotal)
    Total.set(OverAllCost)


